def login_attribute(client):
    # Gets the login attempts from system settings and checks
    login_attempt = sys_attributes(client)
    login_attempts = login_attempt.get_login_attempts()
    if login_attempts is None:
        login_attempts = 0
    return login_attempts


def pwd_attribute(client):
    password = sys_attributes(client)
    pwd_policy = password.get_pwd_policy()
    return pwd_policy

