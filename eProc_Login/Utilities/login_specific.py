class UserFncts(ModelForm):

    # User login function
    @staticmethod
    def login_user(request, email, password):
        """
        :param request: Gets data from UI and validates email id, password.
        :param email: Gets Users email from login page
        :param password: Gets Users password from login page
        :return: Returns user details
        """

        user = authenticate(request, email=email, password=password)
        if user is not None:
            login(request, user)
            return user

    # User logout function
    @staticmethod
    def logout_user(request):
        clear_user_info()
        logout(request)


